guard_positions = []
def guard_move(guard_cord, maxY, maxX, guard, total):
    y, x = guard_cord

    if guard_cord not in guard_positions:
        guard_positions.append(guard_cord)
        grid[y][x] = "X"
        total += 1

    if guard == "^":
        if y > 0:
            if grid[y-1][x] == "#":
                guard = ">"
                x += 1
            else:
                y -= 1
        elif y == 0:
            print("game ended")
            return total, guard, guard_cord, True
        else:
            guard = ">"
            x += 1

    elif guard == ">":
        if x < maxX :
            if grid[y][x+1] == "#":
                guard = "v"
                y += 1
            else:
                x += 1
        elif x == maxX:
            print("game ended")
            return total, guard, guard_cord, True
        else:
            guard = "v"
            y += 1

    elif guard == "<":
        if x > 0:
            if grid[y][x - 1] == "#":
                guard = "^"
                y -= 1
            else:
                x -= 1
        elif x == 0:
            print("game ended")
            return total, guard, guard_cord, True
        else:
            y -= 1

    elif guard == "v":
        if y < maxY:
            if grid[y + 1][x] == "#":
                guard = "<"
                x -= 1
            else:
                y += 1

        elif y == maxY:
            print("game ended")
            return total, guard, guard_cord, True
        else:
            guard = "<"
            x -= 1


    guard_cord = (y, x)
    print(f"Guard moved to: {guard_cord}")


    if y > maxY or y < 0 or x > maxX or x < 0:
        print ("game ended")
        print(total)
        return total, guard, guard_cord, True

    return total, guard, guard_cord, False


